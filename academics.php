<div class="transbox" style="color:white;padding:50px;  background-color: hsla(0,0%,0%,0.9); margin-left:10%;margin-right:10%; text-align:justify;"> 

This program is a first-of-its-kind in India. Instead of centering on a traditional model of lectures and tests, this program employs a learn-by-doing curriculum that gives students the on-the-job experience global employers look for.

Students in this innovative program role-play as software developers at a fictional company, while a faculty member role-plays their manager. Together the student team and faculty member are introduced to a client and given an array of real problems to solve. Some problems are small, and some require significant time and effort. Few, if any, will be able to be completed the night before they are due.

Because the work is real and but the situation is fictionalized, MM(DU) is able to create a safe space where students can take risks and stretch themselves without risk of failure. Students can work together to try new things and see whether or not they work. If students fail, the team then works with the manager (faculty) to keep trying new options until success is reached. Students are free to learn and grow without the fear of making a career-ending error, all while under the watchful eye and guidance of an experienced mentor.

Graduates will be confident in their ability to add immediate value to an employer because they will already have practiced and exceled at job tasks during their university time.


